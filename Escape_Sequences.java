package com.java;

public class Escape_Sequences {

	public static void main(String[] args) {

		System.out.println("Hi I'm Ram, and I'm" + "\n" + "new to Java");
		System.out.println("\t" + "Hi I'm David Beco");
		System.out.println("Hi Syed" + "\b" + " " + "Farith");
		System.out.println("Ram is there" + "\r" + "How are you ?");
		System.out.println("Ram is there" + "\f" + "How are you ?");
		System.out.println("Hi Welcome to \"goa\".");
		System.out.println("I Quit from \'my Job\'.");
		System.out.println("\\I dont know what \\to do");

	}

}
